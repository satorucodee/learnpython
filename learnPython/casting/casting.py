
# python casting

## specify a variable type
### There may be times when you want to specify a type on to a variable.
### This can be done with casting.

# ⚡ Intergers
x = int(3) # x will be 3
y = int(3.0) # y will be 3
z = int("3")  # z will be 3


# ⚡ Floats
a = float(3) # a will be 3.0
b = float("3.0") # b will be 3.0
c = float("3")  # c will be 3.0


# ⚡ Strings
j = str(10) # j will be "10"
k = str(10.10) # k will be "10.10"
l = str("s1")  # l will be "s1"
