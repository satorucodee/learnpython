
# Escape characters
### An escape character is a backslash \ followed by the character you want to insert.

txt = "We are the so-called \"vikings\" from the north"
print(txt)

print("hello\n wolrd") # \n for new line

# \'	Single Quote
# \\	Backslash
# \n	New Line
# \r	Carriage Return
# \t	Tab
# \b	Backspace
# \f	Form Feed
# \ooo	Octal value
# \xhh	Hex value
